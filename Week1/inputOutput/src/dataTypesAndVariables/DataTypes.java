package dataTypesAndVariables;

public class DataTypes {
    public static void main(String[] args) {
            // initials
            char myFirstNameInitial = 'M';
            char myLastNameInitial = 'I';
            // Population in Germany
            int populationInGermany = 83000000;
            // Population on earth
            long populationOnEarth = 7800000000L;
            // Is currently daytime?
            boolean isDaytime = true;
            // Goal strike quote (goals per game) of Mario Gomez at Bayern München
            double marioGomezGoalsPerGame = 0.5;
            // Length of the java program in weeks
            short javaProgramLengthWeeks = 14;
            // The mathematical number PI
            float pi = 3.14159f;

            // Output
            System.out.println("My First Name Initial:" + myFirstNameInitial);
            System.out.println("My Last Name Initial: " + myLastNameInitial);
            System.out.println(" Population in Germany: " + populationInGermany);
            System.out.println("Population on Earth: " + populationOnEarth);
            System.out.println("Is currently daytime? " + isDaytime);
            System.out.println("Goal strike quote of Mario Gomez at Bayern München: " + marioGomezGoalsPerGame);
            System.out.println("Length of the Java program in weeks: " + javaProgramLengthWeeks);
            System.out.println("The mathematical number PI: " + pi);



    }
}
